    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        // validate if null
        if(fieldFirstName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter first name!");
            return ;
        }

        if(fieldLastName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter last name!");
            return ;
        }

        if(fieldDate.getDate() == null){
            JOptionPane.showMessageDialog(null, "Choose date!");
            return ;
        }

        if(fieldPetName.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter pet name!");
            return ;
        }

        if(fieldPetAge.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter age!");
            return ;
        }

        //        if(fieldPetGender.getText().trim().isEmpty()){
            //            JOptionPane.showMessageDialog(null, "Enter gender!");
            //            return ;
            //        }

        if(fieldPetType.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter pet type!");
            return ;
        }

        if(fieldPetBreed.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter breed!");
            return ;
        }

        if(fieldPlan.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Choose an insurance plan!");
            return ;
        }

        // create applicant and pet info
//        Applicant a = business.getApplicantsDirectory().createApplicant(fieldFirstName.getText(),
//            fieldLastName.getText(), fieldDate.getDate());
//
//        Pet p = a.createPet(fieldPetName.getText(), Integer.parseInt(fieldPetAge.getText()),fieldPetGender.isSelected() ,
//            fieldPetType.getText() , fieldPetBreed.getText());


        // update info 
        selectedApplicant.setFirstName(fieldFirstName.getText());
        selectedApplicant.setLastName(fieldLastName.getText());
        selectedApplicant.setApplicationDate(fieldDate.getDate());
        
        selectedApplicant.getPet().setName(fieldPetName.getText());
        selectedApplicant.getPet().setAge(Integer.parseInt(fieldPetAge.getText()));
        selectedApplicant.getPet().setType(fieldPetType.getText());
        selectedApplicant.getPet().setBreed(fieldPetBreed.getText());
        selectedApplicant.getPet().setGender(fieldPetGender.isSelected());
        selectedApplicant.getPet().setInsurancePlan((Insurance) fieldPlan.getSelectedItem());

        JOptionPane.showMessageDialog(null, "Add successfully!");
        display();
        
        
        fieldFirstName.setText("");
        fieldLastName.setText("");
        fieldDate.setDate(null);
        fieldPetName.setText("");
        fieldPetType.setText("");
        fieldPetAge.setText("");
        fieldPetBreed.setText("");
        fieldPetGender.setSelected(false);
        fieldPlan.setSelectedIndex(0);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void comboApplicantActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboApplicantActionPerformed
        // TODO add your handling code here:
        if(comboApplicant.getSelectedIndex() == 0) return ;
        
        selectedApplicant = (Applicant) comboApplicant.getSelectedItem();
        fieldFirstName.setText(selectedApplicant.getFirstName());
        fieldLastName.setText(selectedApplicant.getLastName());
        fieldDate.setDate(selectedApplicant.getApplicationDate());
        fieldPetName.setText(selectedApplicant.getPet().getName());
        fieldPetAge.setText("" + selectedApplicant.getPet().getAge());
        fieldPetType.setText(selectedApplicant.getPet().getType());
        fieldPetBreed.setText(selectedApplicant.getPet().getBreed());
        
        fieldPetGender.setSelected(selectedApplicant.getPet().isGender());
        fieldPlan.setSelectedItem(selectedApplicant.getPet().getInsurancePlan());
        
        
        
    }//GEN-LAST:event_comboApplicantActionPerformed


