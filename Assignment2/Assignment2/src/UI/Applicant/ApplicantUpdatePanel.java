    public void display(){
        // insurance plan
        for(int i=fieldPlan.getItemCount()-1;i>0;i--){
            fieldPlan.removeItemAt(i);
        }
        
        for(Insurance ins:business.getInsuranceDirectory().getInsuranceCatalog()){
            fieldPlan.addItem(ins);
        }
        
        // application combo
                
        for(int i=comboApplicant.getItemCount()-1;i>0;i--){
            comboApplicant.removeItemAt(i);
        }
        
        for(Applicant a:business.getApplicantsDirectory().getApplicantCatalog()){
            comboApplicant.addItem(a);
        }
        
    }
